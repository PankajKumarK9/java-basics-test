package com.sunglowsys.Variables;

public class Variables {
    public static void main(String[] args) {

        int x;
        x=5;
        System.out.println(x);
        // 1 gb = 1000mb
        //1mb = 1000kb

        x = 400;
        System.out.println(x);

        // new condition
        System.out.println(x+1);

        // new condition
        x = x + 5;
        System.out.println(x);


        // new condition
        x  = x- 10;
        System.out.println(x);

    }

}

