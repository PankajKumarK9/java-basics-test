package com.sunglowsys.Opreator;

public class ArithmeticOperations {
    public static void main(String[] args) {

        int x = 10;
        int y = 3 ;

        System.out.println(x-y);
        System.out.println(x+y);
        System.out.println(x*y);

        // cumputer me agar integer ko integer k sath opretion performe karne par integer value hi ayegi
        System.out.println(x/y);

    }
}
